package vo;

public class WebBusinessVO {
	
	String id;
	String name;
	String info;
	
	/**
	 * 网站营销人员的VO类
	 * @param id 网站营销人员的id
	 * @param name 网站营销人员的姓名
	 * @param info 网站营销人员的信息
	 * @author Rukawa
	 */
	public WebBusinessVO(String id, String name, String info) {
		this.id = id ;
		this.name = name ;
		this.info = info ;
	}
	
}
